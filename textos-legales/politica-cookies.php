<h2>Política de cookies</h2>

<h3>1. ¿Qué son las cookies y para qué se utilizan?</h3>

<p>Las cookies son pequeños archivos de texto que se generan cuando un usuario visita un sitio web y se almacenan en su ordenador o dispositivo móvil. Las cookies se utilizan para almacenar información sobre la visita del usuario al sitio web, como sus preferencias de idioma, tamaño de letra y otras opciones de visualización. Esto puede hacer que las visitas posteriores al sitio web sean más fluidas y eficientes.</p>

<h3>2. ¿Qué tipos de cookies utiliza este sitio web?</h3>


<p>En este momento, este sitio web no utiliza cookies analíticas ni cookies comerciales. Tampoco utiliza cookies propias.</p>

<p>Sin embargo, es posible que en el futuro se implementen cookies propias para mejorar la experiencia del usuario o para ofrecer funcionalidades adicionales. Si esto sucediera, se actualizaría esta política de cookies para reflejar los cambios.</p>


<h3>3. Cookies de terceros</h3>


<p>Este sitio web utiliza cookies de reCAPTCHA Enterprise, un servicio de Google que ayuda a proteger el sitio web del spam y el abuso. reCAPTCHA Enterprise utiliza una cookie llamada "_GRECAPTCHA" para realizar su análisis de riesgo.</p>

<p>Puede encontrar más información sobre las cookies de reCAPTCHA Enterprise en la siguiente página: <a href="https://policies.google.com/technologies/cookies" title="Enlace a la página sobre las cookies de Google" target="_blank">https://policies.google.com/technologies/cookies</a></p>

<h3>4. ¿Cómo puedo gestionar las cookies?</h3>


<p>El usuario puede configurar su navegador para aceptar o rechazar todas las cookies o para aceptar solo cookies de sitios web específicos. También puede eliminar las cookies que ya se han almacenado en su ordenador o dispositivo móvil.</p>

<p>La forma de gestionar las cookies depende del navegador que utilice. Para más información, consulte la documentación de su navegador.</p>

